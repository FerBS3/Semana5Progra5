package com.modelos;

public class Usuario {
    private String nombre;
    private int edad;
    private int ingresos;
    private int altura;
    private String genero;

    public Usuario(String nombre, int edad, int ingresos, int altura, String genero) {
        this.nombre = nombre;
        this.edad = edad;
        this.ingresos = ingresos;
        this.altura = altura;
        this.genero = genero;
    }
}
